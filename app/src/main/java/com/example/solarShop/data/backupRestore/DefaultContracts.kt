// DefaultContracts.kt
// 👇 PartySpec را با فیلدهای واقعی Entity یکی کن )
data class PartySpec(
    val role: String, // "employer" | "contractor"
    val fullName: String? = null,
    val fatherFullName: String? = null,
    val nationalId: String? = null,
    val companyName: String? = null,
    val address: String? = null,
    val phone: String? = null
)

data class SectionSpec(
    val orderNo: Int,
    val title: String,
    val body: String,
    val isDefaultVisible: Boolean = true,
    val isRequired: Boolean = false
)

// 👇 برای تبصره‌ها (Notes) هم Spec اضافه می‌کنیم
data class NoteSpec(
    val sectionOrderNo: Int, // تبصره متعلق به کدام ماده است (با orderNo همان ماده)
    val orderNo: Int,        // شماره تبصره در همان ماده (برای نمایش "تبصره 3-5")
    val title: String? = null,
    val body: String
)

data class TemplateSpec(
    val title: String,
    val description: String?,
    val parties: List<PartySpec>,
    val sections: List<SectionSpec>,
    val notes: List<NoteSpec> = emptyList() // ⬅️ جدید
)


